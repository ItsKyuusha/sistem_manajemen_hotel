    private void tb_cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tb_cariActionPerformed
        try {
            java.sql.Connection conn=(java.sql.Connection)sistemmanajemenhotel.Koneksi.koneksiDB();
            String sql = "Select * from kamar where nomor_kamar='"+cari.getText()+"'";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery(sql);
            if (rs.next()){
                NOMOR_KAMAR.setText(rs.getString(1));
                NAMA_TAMU.setText(rs.getString(2));
                LAMA_INAP.setText(rs.getString(3));
                JOptionPane.showMessageDialog(null,"Data Kamar : "
                    + cari.getText() + " ditemukan");
            }
        } catch (Exception e){}
        cari.requestFocus();
    }//GEN-LAST:event_tb_cariActionPerformed

    private void tb_checkinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tb_checkinActionPerformed
        try {
            java.sql.Connection conn = (java.sql.Connection) sistemmanajemenhotel.Koneksi.koneksiDB();
            String sql = "UPDATE kamar SET status='terisi' WHERE nomor_kamar=?";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);

            pst.setString(1, NOMOR_KAMAR.getText());
            
            // Menjalankan perintah update
            pst.executeUpdate();

            JOptionPane.showMessageDialog(null, "Pemesanan berhasil checkin");
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Pemesanan gagal checkin");
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_tb_checkinActionPerformed

