    private void tb_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tb_editActionPerformed
        try {
            java.sql.Connection conn = (java.sql.Connection) sistemmanajemenhotel.Koneksi.koneksiDB();
            String sql = "UPDATE pemesanan SET nama_tamu=?, nomor_kamar=?, tipe_kamar=?, lama_inap=?, harga=?, biaya_total=? WHERE id_pemesanan=?";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);

            // Mengatur nilai untuk parameter
            pst.setString(1, NAMA_TAMU.getText());
            pst.setString(2, NOMOR_KAMAR.getText());
            pst.setString(3, TIPE_KAMAR.getText());
            pst.setString(4, LAMA_INAP.getText());
            pst.setString(5, HARGA.getText());
            pst.setString(6, TOTAL_BIAYA.getText());
            pst.setInt(7, Integer.parseInt(ID_PEMESANAN.getText()));

            // Menjalankan perintah update
            pst.executeUpdate();

            JOptionPane.showMessageDialog(null, "Data berhasil di Koreksi");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Proses Edit data Gagal");
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_tb_editActionPerformed

        public void tampil_data(){
    DefaultTableModel tabel=new DefaultTableModel();
    tabel.addColumn("ID Pemesanan");
    tabel.addColumn("Nama Tamu");
    tabel.addColumn("Nomor Kamar");
    tabel.addColumn("Tipe Kamar");
    tabel.addColumn("Lama Inap");
    tabel.addColumn("Harga");
    tabel.addColumn("Biaya Total");
    try {
        java.sql.Connection conn = (java.sql.Connection) sistemmanajemenhotel.Koneksi.koneksiDB();
        String sql = "select * from pemesanan";
        java.sql.PreparedStatement pst= conn.prepareStatement ( sql);
        ResultSet rs = pst.executeQuery( sql);
      while(rs.next())
        {
        tabel.addRow(new Object[]{
            rs.getString(1),
            rs.getString(2),
            rs.getString(3),
            rs.getString(4),
            rs.getString(5),
            rs.getString(6),
            rs.getString(7)});
        }
        jTable1.setModel(tabel);
        }
    catch (Exception e){
    }
}
    
