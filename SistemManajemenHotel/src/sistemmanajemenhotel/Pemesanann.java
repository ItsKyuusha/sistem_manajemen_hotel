    private void tb_cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tb_cariActionPerformed
        try {
            java.sql.Connection conn=(java.sql.Connection)sistemmanajemenhotel.Koneksi.koneksiDB();
            String sql = "Select * from pemesanan where id_pemesanan='"+cari.getText()+"'";
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery(sql);
            if (rs.next()){
                ID_PEMESANAN.setText(rs.getString(1));
                NAMA_TAMU.setText(rs.getString(2));
                NOMOR_KAMAR.setText(rs.getString(3));
                TIPE_KAMAR.setText(rs.getString(4));
                LAMA_INAP.setText(rs.getString(5));
                HARGA.setText(rs.getString(6));
                TOTAL_BIAYA.setText(rs.getString(7));
                JOptionPane.showMessageDialog(null,"Data Pemesanan : "
                    + cari.getText() + " ditemukan");
            }
        } catch (Exception e){}
        cari.requestFocus();
    }//GEN-LAST:event_tb_cariActionPerformed

