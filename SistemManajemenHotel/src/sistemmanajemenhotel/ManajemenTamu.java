    public void tampil_data(){
    DefaultTableModel tabel=new DefaultTableModel();
    tabel.addColumn("ID Tamu");
    tabel.addColumn("Nama Tamu");
    tabel.addColumn("No. KTP");
    tabel.addColumn("Kontak");
    try {
        java.sql.Connection conn = (java.sql.Connection) sistemmanajemenhotel.Koneksi.koneksiDB();
        String sql = "select * from tamu";
        java.sql.PreparedStatement pst= conn.prepareStatement ( sql);
        ResultSet rs = pst.executeQuery( sql);
      while(rs.next())
        {
        tabel.addRow(new Object[]{
            rs.getString(1),
            rs.getString(2),
            rs.getString(3),
            rs.getString(4)});
        }
        jTable1.setModel(tabel);
        }
    catch (Exception e){
    }
}
    
